const found = document.querySelectorAll(".found");

found.forEach((input) => {
  input.addEventListener("input", function () {
    if (this.value.toLowerCase() === "found") {
      this.style.border = "2px solid rgb(70, 134, 7)";
      this.style.backgroundColor = "#ADFF2F";
      this.style.color = "rgb(43, 83, 4)";
    }
  });
});

